                <h2 class="blog-post-title">Sejarah</h2>
                <p class="blog-post-meta"> Profile / Sejarah </p>
        
                <p>Universitas Pendidikan Indonesia (UPI) sebagai universitas pelopor dan unggul perlu berkontribusi dalam bidang tersebut dan juga dalam rangka UPI sebagai Center of Excellence bidang Pendidikan Vokasi. Kemudian, dalam rangka mempersiapkan sumberdaya manusia sektor kemaritiman pada era revolusi industri 4.0 UPI pada tahun 2019 berencana membuka program studi Sistem Informasi Kelautan.</p>
                <p>Sistem Informasi Kelautan merupakan bagian dari Ilmu Kelautan yang fokus pada analisis data kelautan baik numerik maupun spasial untuk kemudian diintegrasikan ke dalam suatu sistem informasi yang dikembangkan sebagai dasar pengambilan keputusan. Sumberdaya manusia yang terampil dalam hal tersebut sangat diperlukan dalam pembangunan di sektor kemaritiman pada industri 4.0.</p>
                <hr>
                <h2 class="blog-post-title">Visi dan Misi</h2>
                <p class="blog-post-meta"> Profile / Visi dan Misi </p>
                <p class="h6">Visi Keilmuaan</p>
                <p>Mengembangkan dan mengimplementasikan sistem informasi untuk mendukung perkembangan teknologi pada bidang kelautan dan menghasilkan lulusan yang berdayasaing serta ahli dalam pengolahan data numerik maupun spatial.</p>
                <p class="h6">Misi</p>
                <p><ul>
                  <li>Menyelenggarakan layanan pendidikan dan pengajaran berstandar nasional bidang keahlian sistem informasi kelautan,</li>
                  <li>Melaksanakan penelitian di bidang sistem informasi kelautan dan inovasinya dalam upaya mengembangkan potensi sumberdaya hayati pesisir dan laut di daerah dan nasional,</li>
                  <li>Menerapkan teknologi informasi kelautan yang terpadu, produktif dan terukur dalam pendidikan, penelitian, dan pengabdian serta pemberdayaan kepada masyarakat berbasis hasil penelitian.</li>
                  <li>Mengembangkan dan menyebarluaskan keilmuan dan teknologi bidang kelautan yang berbasis pada kebutuhan masyarakat dan industri.</li>
                </ul></p>
